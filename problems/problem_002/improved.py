# 코드의 개선 사항 및 리팩토링

"""
코드의 개선 사항 :
1. c 입력 검증 로직에 버그가 있다.

`if c <= 100:
    break`

이 조건만 확인하면 c < a 인 경우도 통과하여 문제 조건에 위반된다
a < c <= 100 로 수정

2. while True + if 구조를 중첩할 필요없이 while True 안의 if문만 사용하여
코드의 가독성을 높힐 수 있다

3. 입력 검증이 일관되지 않는다
a : 1 이상만 검사
c : 100 이하 + a보다 큰지 검사
여기에 안전성 측면으로 c >= 1도 추가하면 좋다

"""

# 리팩토링 버전

# a 입력
while True:
    a = int(input("직각 삼각형의 한 변의 길이(정수)를 입력하세요 : "))
    if a >= 1:
        break
    print("1 이상의 수로 다시 입력해주세요")

# c 입력
while True:
    c = int(input("직각 삼각형의 빗변의 길이(정수)를 입력하세요 : "))
    if a < c <= 100:
        break
    print("a보다 크고 100 이하의 수로 다시 입력해주세요")

# 결과 계산
result = c**2 - a**2

# 출력
print(f"직각삼각형의 다른 변의 제곱값 : {result}")