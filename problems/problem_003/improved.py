# 코드의 개선사항 및 리팩토링

"""
코드의 개선사항 :

1. 리스트 대신 정수 누적이 더 효율적
x, y 리스트에 값을 계속 추가한 뒤 sum()을 쓰는 것 보다 `x += 1, y-= 1` 이런 식으로
바로 누적하는 게 더 깔끔하다

2. 딕셔너리 구조 개선
지금 딕셔너리는 방향별로 값이 겹쳐서 의미가 조금 애매하다
(x, y)좌표로 한 번에 표현하면 훨씬 직관적

3. 조건문 단순화 가능
`if i == "N" or i == "S":` 이런 식 대신 딕셔너리 하나로 처리하면 조건문 자체가
필요없다

4. 입력 검증 보완
현재는 길이만 검사하고 있는데, 문제 조건 상 "N", "S", "E", "W"만 들어와야하므로
이것도 체크하면 안전하다
"""

# 리팩토링 코드

# 방향별 이동 좌표 (x, y)
directions = {
    "N": (0, 1),
    "S": (0, -1),
    "E": (1, 0),
    "W": (-1, 0)
}

# 입력 받기
while True:
    sannpo = input("산책루트를 입력하세요 : ")
    
    if len(sannpo) > 20:
        print("문자열 개수를 20개 초과하여 작성할 수 없습니다")
        continue
    
    # 유효 문자 검사
    if not all(c in directions for c in sannpo):
        print("N, S, E, W만 입력 가능합니다")
        continue
        
    break

# 좌표 초기화
x, y = 0, 0

# 이동 계산
for move in sannpo:
    dx, dy = directions[move]
    x += dx
    y += dy

# 출력
print(f"출발지로부터 이동한 거리는 {x}, {y}")